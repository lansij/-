这里补充一个小知识：心理学上有一个白熊效应，心理学家让参与者尝试 “不要想白熊”，结果发现人们反而会更频繁想到白熊，而允许正常思考时，白熊出现的频率则降低。

 

这是因为当我们试图压抑某个想法时，大脑会启动 “监控系统”，不断检查自己是否在想被禁止的事物。

然而这种 “监控” 行为本身就会让该想法在意识中保持活跃，尽管意识上反复告诉自己“不要想太多”，却“越压抑越想起、越想起越焦虑”。

 

因此，当我们弄清楚具体忧虑的事情，直接设想最坏结果并试着勇敢接纳它时，反而能放松下来，从而有空间调动大脑资源来思索如何应对。

02
 

林语堂先生在他写的《生活的艺术》中也提到，“从心理学角度来讲，内心安宁，能够面对最差的结果，就可以挖掘出人的潜能”。

 

于是，我们来到应对职场焦虑的万能策略的第三步，也是最重要的一步：集中时间精力，从一小步开始，改变那最坏的结果。

 

还是回到小A的例子，我问她：“你现在平静一些了，还有一个月时间，能想到什么办法可以使你顺利通过试用期吗？”

 

小A说，“因为我才来一个多月，对老板还不熟悉，就凭经验去做了。如果他下次再找我办事，我可能会先问清楚他的需求。

 

其实这件事老板虽然说他自己搞定，但流程应该还是需要我来上的，也许我可以马上找资深同事寻求一些帮助和意见，上小红书或者人工智能找一些灵感和参考，汇总几个像样的选择重新和老板汇报，说不定能消除这件事上他对我的看法。”

 

小A在自我梳理的过程中，语气越来越平稳淡定，已经不再是局促不安的样子，思路越来越清晰，不再深陷没办好事、惴惴不安等待被审判的沼泽里。

 

而在小B的例子中，他焦虑的最坏的结果，要么是不配合医馆的业绩提升计划、逐渐被边缘化但不至于失业，要么是像对标医生那样业绩翻倍但身体彻底垮掉。

 

把这些最糟糕的情况说出来了之后，他当即决定接受友馆抛来的橄榄枝，并约定对方当周的时间去拜访。此前他一直借口自己没时间。

 

仅仅是到这一步，他说感觉胸口的石头轻了不少。因为这一小步，让他开始重构自己的职业发展路径，主动掌握选择权和确定性，而不是被动地徘徊在如何满足医馆要求的痛苦中。

 

就像小C，上周末他终于鼓起勇气提了加班申请，他在上面详细列明了周末需要加班的原因、以及加班计划达成的事项。

 

点击提交后，他感到长舒一口气。后来事实证明，老板对于加班申请并未有任何的质疑，很快通过了审批，那一刻他感觉前所未有的有动力。

 

在此之前，他总担心提加班老板会觉得他计较或者没能在工作时间完成任务，而长期默默加班他又会感觉没被看见，觉得委屈。虽然很小一件事，却是真实的困扰。

03
 

其实作为十几年的职场人，我很明白最容易受到职场焦虑伤害的时候，并不是在一天的工作行动中，而是在工作做完了之后。那时我们的思想会混乱起来，容易胡思乱想，会把曾经出现过的每一个小错误都加以夸大。

 

所以，摆脱职场焦虑的万能策略，就是：

 

1、向自己发问，我具体焦虑的是什么？可能发生的最坏结果是什么？

——通过具体化对冲焦虑带来的不确定性。

2、鼓起勇气，做好全然接纳最坏结果的准备。

——对抗强化焦虑，而接纳让焦虑消融。许多人的生活毁于焦虑，正是因为不愿接受最坏的结果，不断地徘徊在痛苦之中。

3、竭尽全力，想办法改善最坏的结果。

——特别提醒，这一步不只是制定计划，更重要的是行动起来，做些有用的事情。很多时候，仅仅只是迈出一小步，一切都将发生改变。

