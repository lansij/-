回到小A的例子，我们沟通之后，她思忖着告诉我：“这次是我没有办好老板交代的那件小事，如果再有类似情况，老板很有可能会炒了我。果真如此的话，我就当给自己放了个寒假，春节后趁返工潮我再找新的工作，但现在大环境不好，可能有几个月的空窗期。其实这次事情也提醒了我的不足，我大概率之后还会从事相关的工作，就当吃一堑长一智了。”

 

